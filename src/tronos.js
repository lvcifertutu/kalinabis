let domainParticles = [];
let domainCtx = null;
let domainW = 0, domainH = 0;
let currentDomainDeidad = 'tutu';

function initDomainCanvas() {
  const canvas = document.getElementById('domain-canvas');
  if (!canvas) return;
  domainCtx = canvas.getContext('2d');
  function resizeDomain() {
    domainW = canvas.width  = window.innerWidth;
    domainH = canvas.height = window.innerHeight;
  }
  resizeDomain();
  window.addEventListener('resize', resizeDomain);
  domainLoop();
}

class DomainParticle {
  constructor(tipo) {
    this.tipo = tipo;
    this.reset();
  }
  reset() {
    const tipo = this.tipo;
    if (tipo === 'ember') {
      this.x = Math.random() * domainW;
      this.y = domainH * 0.6 + Math.random() * domainH * 0.4;
      this.vx = (Math.random() - 0.5) * 0.8;
      this.vy = -(0.6 + Math.random() * 1.8);
      this.r = 1 + Math.random() * 2.5;
      this.alpha = 0.7 + Math.random() * 0.3;
      this.life = 0; this.maxLife = 80 + Math.random() * 120;
      this.color = [196 + Math.random()*30, 130 + Math.random()*40, 50];
    } else if (tipo === 'geo') {
      this.x = Math.random() * domainW;
      this.y = Math.random() * domainH;
      this.vx = (Math.random() - 0.5) * 0.3;
      this.vy = (Math.random() - 0.5) * 0.3;
      this.r = 2 + Math.random() * 3;
      this.alpha = 0.3 + Math.random() * 0.4;
      this.life = 0; this.maxLife = 200 + Math.random() * 300;
      this.angle = Math.random() * Math.PI * 2;
      this.color = [184 + Math.random()*20, 160 + Math.random()*20, 220];
    } else if (tipo === 'rain') {
      this.x = Math.random() * domainW;
      this.y = -20;
      this.vx = 0.8 + Math.random() * 0.5;
      this.vy = 8 + Math.random() * 6;
      this.len = 8 + Math.random() * 18;
      this.alpha = 0.2 + Math.random() * 0.4;
      this.life = 0; this.maxLife = 60 + Math.random() * 40;
      this.color = [90, 155, 196];
    } else if (tipo === 'leaf') {
      this.x = Math.random() * domainW;
      this.y = -20;
      this.vx = (Math.random() - 0.5) * 1.5;
      this.vy = 0.8 + Math.random() * 1.5;
      this.r = 3 + Math.random() * 5;
      this.alpha = 0.3 + Math.random() * 0.4;
      this.rot = Math.random() * Math.PI * 2;
      this.rotV = (Math.random() - 0.5) * 0.08;
      this.life = 0; this.maxLife = 180 + Math.random() * 200;
      this.color = [80 + Math.random()*40, 120 + Math.random()*40, 60 + Math.random()*30];
    }
  }
  update() {
    this.life++;
    const tipo = this.tipo;
    if (tipo === 'ember') {
      this.x += this.vx + Math.sin(this.life * 0.05) * 0.4;
      this.y += this.vy;
      this.alpha -= 0.006;
    } else if (tipo === 'geo') {
      this.angle += 0.008;
      this.x += this.vx + Math.cos(this.angle) * 0.2;
      this.y += this.vy + Math.sin(this.angle) * 0.2;
      if (this.x < 0) this.x = domainW;
      if (this.x > domainW) this.x = 0;
      if (this.y < 0) this.y = domainH;
      if (this.y > domainH) this.y = 0;
    } else if (tipo === 'rain') {
      this.x += this.vx;
      this.y += this.vy;
    } else if (tipo === 'leaf') {
      this.x += this.vx + Math.sin(this.life * 0.03) * 0.6;
      this.y += this.vy;
      this.rot += this.rotV;
    }
    if (this.life >= this.maxLife || this.alpha <= 0 || this.y > domainH + 30 || this.y < -50) this.reset();
  }
  draw() {
    if (!domainCtx) return;
    const ctx = domainCtx;
    const [r,g,b] = this.color;
    ctx.save();
    ctx.globalAlpha = Math.max(0, this.alpha);
    const tipo = this.tipo;
    if (tipo === 'ember') {
      const gr = ctx.createRadialGradient(this.x,this.y,0,this.x,this.y,this.r*2);
      gr.addColorStop(0, `rgba(${r},${g},${b},1)`);
      gr.addColorStop(1, `rgba(${r},${g},${b},0)`);
      ctx.fillStyle = gr;
      ctx.beginPath(); ctx.arc(this.x,this.y,this.r*2,0,Math.PI*2); ctx.fill();
    } else if (tipo === 'geo') {
      ctx.strokeStyle = `rgba(${r},${g},${b},${this.alpha})`;
      ctx.lineWidth = 0.8;
      ctx.beginPath();
      for (let i=0; i<4; i++) {
        const a = (i/4)*Math.PI*2 + this.angle;
        const x = this.x + Math.cos(a)*this.r;
        const y = this.y + Math.sin(a)*this.r;
        i===0 ? ctx.moveTo(x,y) : ctx.lineTo(x,y);
      }
      ctx.closePath(); ctx.stroke();
    } else if (tipo === 'rain') {
      ctx.strokeStyle = `rgba(${r},${g},${b},${this.alpha})`;
      ctx.lineWidth = 0.8;
      ctx.beginPath();
      ctx.moveTo(this.x, this.y);
      ctx.lineTo(this.x + this.vx*this.len/this.vy, this.y + this.len);
      ctx.stroke();
    } else if (tipo === 'leaf') {
      ctx.translate(this.x, this.y);
      ctx.rotate(this.rot);
      ctx.fillStyle = `rgba(${r},${g},${b},${this.alpha})`;
      ctx.beginPath();
      ctx.ellipse(0, 0, this.r, this.r*0.5, 0, 0, Math.PI*2);
      ctx.fill();
    }
    ctx.restore();
  }
}

const DOMAIN_TYPES = {
  isis:'ember', afrodita:'geo', lilith:'rain', artemisa:'leaf', tutu:null, kali:null
};
const DOMAIN_COUNT = { ember:25, geo:30, rain:40, leaf:22 };

function cambiarParticulasDominio(deidad) {
  currentDomainDeidad = deidad;
  const tipo = DOMAIN_TYPES[deidad];
  domainParticles = [];
  if (tipo) {
    const count = DOMAIN_COUNT[tipo] || 25;
    for (let i=0; i<count; i++) domainParticles.push(new DomainParticle(tipo));
  }
}

function domainLoop() {
  if (domainCtx) {
    domainCtx.clearRect(0,0,domainW,domainH);
    domainParticles.forEach(p => { p.update(); p.draw(); });
  }
  requestAnimationFrame(domainLoop);
}

function manifestarTrono(nombre) {
  const trono = document.getElementById('deity-throne');
  const wrap  = document.getElementById('deity-throne-wrap');
  if (!trono) return;
  trono.classList.remove('visible');
  setTimeout(() => {
    trono.innerHTML = TRONOS[nombre] || '';
    const panel = document.querySelector('.panel-deidades');
    const panelW = panel ? panel.offsetWidth : 240;
    wrap.style.left = panelW + 'px';
    requestAnimationFrame(() => {
      requestAnimationFrame(() => {
        trono.classList.add('visible');
      });
    });
  }, 600);
}

function cambiarAtmosfera(nombre) {
  const clases = ['space-isis','space-afrodita','space-lilith','space-artemisa','space-tutu'];
  clases.forEach(c => document.body.classList.remove(c));
  if (nombre && nombre !== 'kali') {
    document.body.classList.add('space-' + nombre);
  }
}
